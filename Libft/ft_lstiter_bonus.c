#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	tmp = lst;
	while (tmp != NULL)
	{
		if (f != NULL)
		{
			f(tmp->content);
		}
		tmp = tmp->next;
	}
}
