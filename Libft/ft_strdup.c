#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*s2;

	if (s1 == NULL)
		return (NULL);
	i = ft_strlen(s1);
	s2 = malloc(sizeof (*s2) * (i + 1));
	if (s2 == NULL)
		return (NULL);
	ft_memcpy(s2, s1, i + 1);
	return (s2);
}
